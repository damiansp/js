npm install x # installs module x
npm install x --save # installs and adds dependency to package.json
npm install x@1.0.3  # spec version
npm install x@"~1.0.0" # all patch versions of 1.0
npm install x@"^1.0.0" # all minor version changes
npm ls                 # list installed packages
npm rm x               # remove x
npm unistall x         # same

